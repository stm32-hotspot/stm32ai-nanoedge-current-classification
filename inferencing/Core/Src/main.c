void FillBuffer(float* buffer, uint32_t size)
{
  uint32_t i = 0;
  uint32_t step = AXIS_NUMBER;

  while (i < size)
  {
    if (sensorDataReady!=RESET)
    {
      sensorDataReady = RESET;
      buffer[i] = (float)HAL_ADC_GetValue(&hadc1);
      i += step;
    }
  }
}

void PrintBuffer(float *buffer, uint32_t size)
{
  uint32_t i = 0;
  uint32_t step = AXIS_NUMBER;

  while (i < size)
  {
    if (i < size-step)
    {
      printf("%d ",(int)buffer[i]);
    }
    else
    {
      printf("%d\n",(int)buffer[i]);
    }
    i += step;
  }
}

